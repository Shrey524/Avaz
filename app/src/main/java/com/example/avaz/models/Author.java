package com.example.avaz.models;

public class Author{
	private String name;
	private String location;
	private String permalink;
	private String username;

	public String getName(){
		return name;
	}

	public String getLocation(){
		return location;
	}

	public String getPermalink(){
		return permalink;
	}

	public String getUsername(){
		return username;
	}
}
